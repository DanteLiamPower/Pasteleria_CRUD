    private void btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualizarActionPerformed
        int filaSeleccionada = tb_ver.getSelectedRow();
        
        if (filaSeleccionada >= 0) {
            modeloTabla.setValueAt(txtNombre.getText(), filaSeleccionada, 0);
            modeloTabla.setValueAt(txtID.getText(), filaSeleccionada, 1);
            modeloTabla.setValueAt(txtPrecio.getText(), filaSeleccionada, 2);
            modeloTabla.setValueAt(txtTipo.getText(), filaSeleccionada, 3);
            modeloTabla.setValueAt(txtEstado.getText(), filaSeleccionada, 4);
            JOptionPane.showMessageDialog(null, "Producto actualizado", "Información", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Selecciona una fila para actualizar", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_ActualizarActionPerformed

    private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed
        int filaSeleccionada = tb_ver.getSelectedRow();
        if (filaSeleccionada >= 0) {
            modeloTabla.removeRow(filaSeleccionada);
            JOptionPane.showMessageDialog(null, "Producto eliminado", "Información", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Selecciona una fila para eliminar", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_EliminarActionPerformed
    private void limpiarCampos() {
        txtNombre.setText("");
        txtID.setText("");
        txtPrecio.setText("");
        txtTipo.setText("");
        txtEstado.setText("");
    }
    private void registrarProducto() {

    String nombre = txtNombre.getText();
    String id = txtID.getText();
    String precio = txtPrecio.getText();
    String tipo = txtTipo.getText();
    String estado = txtEstado.getText();

    modeloTabla.addRow(new Object[]{nombre, id, precio, tipo, estado});
}


    public boolean verificacion_de_numeros_negativos() {

        try {
            double precio = Double.parseDouble(txtPrecio.getText().trim());
            if (precio <= 0) {
                JOptionPane.showMessageDialog(null, "No se permiten números negativos o cero en el precio", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Precio incorrecto: formato no válido", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;

    }

