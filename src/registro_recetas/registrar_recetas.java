    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
//validaciones

        if (txtNombre.getText().isEmpty() || txtID.getText().isEmpty()
                || txtPrecio.getText().isEmpty() || txtTipo.getText().isEmpty()
                || txtEstado.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos los campos deben estar llenos", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            boolean datosCorrectos = true;

            if (!validador_de_letras(txtNombre.getText().trim())) {
                JOptionPane.showMessageDialog(null, "Nombre incorrecto: solo se permiten letras", "Advertencia", JOptionPane.WARNING_MESSAGE);
                datosCorrectos = false;
            }

            if (!validador_de_numeros(txtID.getText().trim())) {
                JOptionPane.showMessageDialog(null, "ID incorrecto: solo se permiten números", "Advertencia", JOptionPane.WARNING_MESSAGE);
                datosCorrectos = false;
            }

            if (!validador_de_letras(txtPrecio.getText().trim())) {
                JOptionPane.showMessageDialog(null, "Dificultad: solo se permiten letras", "Advertencia", JOptionPane.WARNING_MESSAGE);
                datosCorrectos = false;
            }
                
            

            if (!validador_de_letras(txtTipo.getText().trim())) {
                JOptionPane.showMessageDialog(null, "Descripcion incorrecto: solo se permiten letras", "Advertencia", JOptionPane.WARNING_MESSAGE);
                datosCorrectos = false;
            }
              if ( !validador_de_estado(txtEstado.getText().trim())) {
                JOptionPane.showMessageDialog(null, "Estado incorrecto: solo se permiten numeros del 1 al 0", "Advertencia", JOptionPane.WARNING_MESSAGE);
                datosCorrectos = false;
            } if(datosCorrectos) {
                registrarProducto();
                limpiarCampos();
              }
        }
        


    }//GEN-LAST:event_btn_registrarActionPerformed

